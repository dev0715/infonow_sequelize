function ISOCodeFromCountryCode(countryCode: string, mobile: string) {
	let matchISO = undefined

	if (countryCode == '1' || countryCode == '+1') {
		matchISO = 'US'

		if (mobile && mobile.length > 3) {
			const first3Digit = mobile.substr(0, 3)
			for (var i = 0; i < CANADA_NPA.length; i++) {
				if (CANADA_NPA[i]!.toString() == first3Digit) {
					matchISO = 'CA'
					return matchISO
				}
			}
		}
		return matchISO
	} else {
		for (var i = 0; i < countriesWithMobileCodes.length; i++) {
			if (countriesWithMobileCodes[i].countryCode == countryCode) {
				matchISO = countriesWithMobileCodes[i].isoCode
				return countriesWithMobileCodes[i].isoCode
			}
		}
		return matchISO
	}
}

export function getCurrencyByCountryCode(countryCode: string) {
	for (let i = 1; i < countriesWithCurrencyCodes.length; i++) {
		if (countriesWithCurrencyCodes[i].Code == countryCode) {
			return countriesWithCurrencyCodes[i].Code
		}
	}
	return 'USD'
}

export function CurrencyByMobileNumber(countryCode: string, mobile: string) {
	const c_code = ISOCodeFromCountryCode(countryCode, mobile)
	if (c_code != undefined) {
		for (let i = 0; i < countriesWithCurrencyCodes.length; i++) {
			if (countriesWithCurrencyCodes[i].CountryCode == c_code) {
				return countriesWithCurrencyCodes[i].Code
			}
		}

		// if not found any
		return 'USD'
	}
	return 'USD'
}
